    private void refreshTable() {
    try {
        Connection con = ConnectionManager.getConnection();
        String query = "SELECT * FROM cred";
        PreparedStatement pstmt = con.prepareStatement(query);
        ResultSet rs = pstmt.executeQuery();

        // Clear existing table data
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // Populate table with data from result set
        while (rs.next()) {
            Object[] row = {
                rs.getString("EmployeeID"),
                rs.getString("emp_fname"),
                rs.getString("emp_lname"),
                rs.getString("LOB"),
                rs.getString("Position"),
                rs.getString("status")
            };
            model.addRow(row);
        }

        // Close resources
        rs.close();
        pstmt.close();
        con.close();
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: Failed to refresh table", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
